package prac;

public class Prac_04 {

	public static void main(String[] args) {
		
		char single = '\''; 				// single = ''';와 같이 할 수 없다.	// ' 이거 한개 출력하는거다.
		String dblQuote = "\"Hello\"";		//Hello 앞뒤로 따옴표를 한거다.
		// 겹따옴표를 출력하려면 이렇게 한다.
		String root = "c:\\"; 		// c:\  이거 출력하는거다.
		System.out.println(single);
		System.out.println(dblQuote);
		System.out.println(root);
		
	}

}
