package ex1;

public class BooleanVar {

	public static void main(String[] args) {
		
		boolean b1 = true;
		boolean b2 = false;
		
		int num = 100;
		
		System.out.println(b1);
		System.out.println(b2);
		System.out.println("============================");
		
		System.out.println(4 > 3);		// >, <, ==, >=, <=   비교연산의 결과는 true, false가 나온다.
		System.out.println(4 < 3);
		System.out.println("============================");
		
		System.out.println(num>20);
		
		
	}

}
