package ex;

public class IEBasic {

	public static void main(String[] args) {
		
		if(true) {
			System.out.println("true 일때 출력");
		}
		
		if(false) {										//false 라서 실행안된다.
			System.out.println("false 일때 출력");
		}
		
		if(false) {
			System.out.println("true 일때 출력");
		} else { 
			System.out.println("false 일때 출력");
		}
		
		if(10>5) {
			System.out.println("10은 5보다 큽니다");
		}
		
		int num = 10;
		
		if(num==5) {
			System.out.println("10은 5와 같습니다");
		}
			
		
		
	}

}
